package com.aominfosystem.pojo;


public class Usercardbag {

  private long id;
  private long fromqq;
  private String cardGroupName;
  private String cardName;
  private String cardDescribe;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public long getFromqq() {
    return fromqq;
  }

  public void setFromqq(long fromqq) {
    this.fromqq = fromqq;
  }


  public String getCardGroupName() {
    return cardGroupName;
  }

  public void setCardGroupName(String cardGroupName) {
    this.cardGroupName = cardGroupName;
  }


  public String getCardName() {
    return cardName;
  }

  public void setCardName(String cardName) {
    this.cardName = cardName;
  }


  public String getCardDescribe() {
    return cardDescribe;
  }

  public void setCardDescribe(String cardDescribe) {
    this.cardDescribe = cardDescribe;
  }

}
